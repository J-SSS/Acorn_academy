package ch02.ex01_print;

public class C01Print01 {
	public static void main(String[] args) {
		// println(값): 출력 후 줄 바꿈
		System.out.println("hello");
		
		// print(): 출력
		System.out.print("hello");
		System.out.println(" world");
		
		System.out.println(); // 줄 바꿈
		
		// printf(): 포맷 형식으로 출력
		System.out.printf("hello");
	}
}
