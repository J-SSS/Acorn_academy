package ch02.quiz.quiz02;

public class Score {
	private String name;
	private String sth;
	private int score;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSth() {
		return sth;
	}
	public void setSth(String sth) {
		this.sth = sth;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
	
}


